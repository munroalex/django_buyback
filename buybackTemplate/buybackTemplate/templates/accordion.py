    csv_data = [row for row in csvdata]
    tree = csv_data[0]

    def insert(tree, node):
        if node["parentGroupID"] == tree["marketGroupID"]:
            if "child" not in tree:
                tree["child"] = [node]
            else:
                tree["child"].append(node)
        elif "child" in tree:
            for c in tree["child"]:
                insert(c, node)

